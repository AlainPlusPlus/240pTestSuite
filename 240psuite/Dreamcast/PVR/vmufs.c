int saveexists()
{
	uint8		*pkg_in = NULL;
	int			pkg_size = 0;
	maple_device_t *vmu;

	vmu = maple_enum_type(0, MAPLE_FUNC_MEMCARD);
	if(!vmu)
		return 0;

	vmufs_init ();

	if(vmufs_read(vmu, VMU_NAME, (void*)&pkg_in, &pkg_size) < 0)
		return 0;

	vmufs_shutdown ();
	free(pkg_in);
	return (pkg_size >> 9);  // return result in blocks
}

int LoadVMUSave(char *error)
{
	vmu_pkg_t		pkg;
	uint8			*pkg_in = NULL;
	int				pkg_size = 0;
	maple_device_t	*vmu;

	vmu = maple_enum_type(0, MAPLE_FUNC_MEMCARD);
	if(!vmu)
	{
		sprintf(error, "#RNo VMU found#R");
#ifdef DCLOAD
		dbglog(DBG_ERROR, "No VMU found for load\n");
#endif
		return 0;
	}

	vmufs_init ();

	if(vmufs_read(vmu, VMU_NAME, (void*)&pkg_in, &pkg_size) < 0)
	{
		sprintf(error, "#RVMU could not be read#R");
#ifdef DCLOAD
		dbglog(DBG_ERROR, "VMU could not be read\n");
#endif
		return 0;
	}

	vmufs_shutdown ();

	if(vmu_pkg_parse(pkg_in, &pkg) != 0)
	{
		free(pkg_in);
		vmufs_delete (vmu, VMU_NAME);
		sprintf(error, "#RCorrupt package, deleted save#R");
#ifdef DCLOAD
		dbglog(DBG_ERROR, "Corrupt package %s, deleted save from VMU\n", VMU_NAME);
#endif
		return 0;
	}

	free(pkg_in);
	pkg_in = NULL;

	if(pkg.data_len != DATA_SIZE)
	{
		sprintf(error, "#RSave length of unexpected size %d#R", pkg.data_len);
#ifdef DCLOAD
		dbglog(DBG_ERROR, "Save length of unexpected size %d\n", pkg.data_len);
#endif
		return 0;
	}

	if(pkg.data[0] != SAVE_NUM)
	{
		sprintf(error, "#RVersion # differs, discarding %d != %d#R", SAVE_NUM, pkg.data[0]);
#ifdef DCLOAD
		dbglog(DBG_ERROR, "VMU save version # differs, discarding %d != %d\n", SAVE_NUM, pkg.data[0]);
#endif
		return 0;
	}

	settings.drawborder = pkg.data[1];
	settings.EnablePAL = pkg.data[2];
	settings.PALStart = pkg.data[3];
	settings.ChangeVideoRegs = pkg.data[4];
	settings.Deflicker = pkg.data[5];
	settings.EnablePALBG = pkg.data[6];
	settings.PalBackR = pkg.data[7] / 100.0;
	settings.PalBackG = pkg.data[8] / 100.0;
	settings.PalBackB = pkg.data[9] / 100.0;
	SetScanlineIntensity((double)pkg.data[10]);
	if(!pkg.data[11])
		ToggleScanlineEvenOdd();
	settings.Dithering = pkg.data[12];
	settings.UseKOSDefaults = pkg.data[13];
	settings.IgnoreFrameBuffer = pkg.data[14];
		
#ifdef DCLOAD
	dbglog(DBG_INFO, "Loaded VMU save version #%d\n", pkg.data[0]);
#endif
	return 1;
}

int WriteVMUSave(int eyecatch, char *error)
{
	vmu_pkg_t		pkg;
	uint8			data[DATA_SIZE], *pkg_out = NULL;
	int				pkg_size = 0, expanded_pkg_size = 0,
					icon = 0, blocks_needed = 0, blocks_toRelease = 0;
	maple_device_t	*vmu;
	unsigned char	*icon_array[ICON_ARRAY_SIZE] = {
						sd_data, smpte_data, sdback_data,
						sdbars_data, sdmono_data };

	if(!error)
		return 0;

	vmu = maple_enum_type(0, MAPLE_FUNC_MEMCARD);
	if(!vmu)
	{
		sprintf(error, "#RFAIL: VMU not found#R");
		return 0;
	}

	memset(&data, 0, sizeof(uint8)*DATA_SIZE);

	data[0] = SAVE_NUM; // version number
	data[1] = settings.drawborder;
	data[2] = settings.EnablePAL;
	data[3] = settings.PALStart;
	data[4] = settings.ChangeVideoRegs;
	data[5] = settings.Deflicker;
	data[6] = settings.EnablePALBG;
	data[7] = settings.PalBackR * 100;
	data[8] = settings.PalBackG * 100;
	data[9] = settings.PalBackB * 100;
	data[10] = (int)GetScanlineIntensity();
	data[11] = ScanlinesEven();
	data[12] = settings.Dithering;
	data[13] = settings.UseKOSDefaults;
	data[14] = settings.IgnoreFrameBuffer;
	
	strcpy(pkg.desc_short, "240p Suite");
	strcpy(pkg.desc_long, "240p Test Suite Options");
	strcpy(pkg.app_id, "240p Suite");
	pkg.icon_cnt = 1;

	srand((int)(time(0) ^ getpid()));
	icon = rand() % ICON_ARRAY_SIZE;

	memcpy(&pkg.icon_pal[0], icon_array[icon], 32);
		pkg.icon_data = icon_array[icon] + 32;

#ifdef DCLOAD
	dbglog(DBG_INFO, "Random Icon #%d\n", icon);
#endif

	pkg.icon_anim_speed = 0;
	switch(eyecatch)
	{
		case 1:
		{
			pkg.eyecatch_type = VMUPKG_EC_256COL;
			pkg.eyecatch_data = dceye;
		}
		break;
		case 2:
		{
			pkg.eyecatch_type = VMUPKG_EC_256COL;
			pkg.eyecatch_data = ksaur;
		}
		break;
		case 0:
		default:
			pkg.eyecatch_type = VMUPKG_EC_NONE;
		break;
	}
	pkg.data_len = DATA_SIZE;
	pkg.data = data;

	if(vmu_pkg_build(&pkg, &pkg_out, &pkg_size) != 0)
	{
		sprintf(error, "#RVMU package creation failed#R");
		return 0;
	}
	
	// Do the padding so that no dirty memory is saved to the VMU
	expanded_pkg_size = ((pkg_size + 511) & ~511);
	if(expanded_pkg_size > pkg_size)
	{
		uint8	*new_pkg_out = NULL;
		
		new_pkg_out = (uint8*)malloc(expanded_pkg_size);
		if(!new_pkg_out)
		{
			free(pkg_out);
			sprintf(error, "#RVMU package padding failed#R");
			return 0;
		}
		memset(new_pkg_out, 0, expanded_pkg_size);
		memcpy(new_pkg_out, pkg_out, pkg_size);
		free(pkg_out);
#ifdef DCLOAD
		dbglog(DBG_INFO, "Padded VMU save from %d bytes to %d bytes\n", pkg_size, expanded_pkg_size);
#endif
		pkg_out = new_pkg_out;
		pkg_size = expanded_pkg_size;
	}
	
	blocks_needed = pkg_size >> 9;
	blocks_toRelease = saveexists();
	vmufs_init ();

#ifdef DCLOAD
	dbglog(DBG_INFO, "VMU Free %d/Overwrite release %d/Blocks Needed %d\n", 
		vmufs_free_blocks(vmu), blocks_toRelease, blocks_needed);
#endif

	if(vmufs_free_blocks(vmu) + blocks_toRelease < blocks_needed)
	{
		free(pkg_out);
		sprintf(error, "#RFAIL: %d blocks needed in VMU#R", blocks_needed);
		return 0;
	}

	if(vmufs_write(vmu, VMU_NAME, pkg_out, pkg_size, VMUFS_OVERWRITE) < 0)
	{
		free(pkg_out);
		sprintf(error, "#RFAIL: Could not save to VMU#R");
		return 0;
	}
	
	vmufs_shutdown ();
	free(pkg_out);
	return 1;
}


